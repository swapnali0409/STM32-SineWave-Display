void draw_grid()
{
    // Draw horizontal grid lines (Green)
    for(int y = 0; y < HEIGHT; y += 20) {
        for(int x = 0; x < WIDTH; x++) {
            ST7789_DrawPixel(x, y, 0x07E0);
        }
    }
    // Draw vertical grid lines (Green)
    for(int x = 0; x < WIDTH; x += 20) {
        for(int y = 0; y < HEIGHT; y++) {
            ST7789_DrawPixel(x, y, 0x07E0);
        }
    }
    // Center line (White)
    for(int x = 0; x < WIDTH; x++) {
        ST7789_DrawPixel(x, HEIGHT/2, 0xFFFF);
    }
}

void generate_sine()
{
    for(int x = 0; x < WIDTH; x++)
    {
        // 2 * PI * x / WIDTH gives exactly one full cycle
        float angle = (2.0f * PI * x) / WIDTH;
        float s = sinf(angle);
        int y = (HEIGHT/2) + (int)(s * 40);  // 40 is the amplitude
        sine_buffer[x] = y;
    }
}

// Global variable to store the old wave so we can erase it specifically
uint8_t old_sine_buffer[WIDTH];

void draw_sine()
{
    for(int x = 0; x < WIDTH; x++)
    {
        // 1. Erase ONLY the old pixel to prevent flickering
        // We set it back to black (0x0000) or redraw the grid pixel if needed
        ST7789_DrawPixel(x, old_sine_buffer[x], 0x0000);

        // 2. Draw the new pixel (White)
        ST7789_DrawPixel(x, sine_buffer[x], 0xFFFF);

        // 3. Save the current position for the next frame's erasure
        old_sine_buffer[x] = sine_buffer[x];
    }
}
